# Npm 使用
# 安装包:

# $ npm install <package>     # 安装在本地项目中
# $ npm install -g <package>  # 安装在全局
# 安装包，并且将其保存你项目中的 package.json 文件:
# $ npm install <package> --save
# 查看 npm 安装的内容:
# $ npm list     # 本地
# $ npm list -g  # 全局
# 查看过期的包（本地或全局）:
# $ npm outdated [-g]
# 更新全部或特别指定一个包:
# $ npm update [<package>]
# 卸载包:

# $ npm uninstall <package>
